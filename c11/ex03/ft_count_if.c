int	ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int	a;

	i = -1;
	a = 0;
	while (tab[++i])
	{
		if ((*f)(&tab[i]) != 0)
			a++;
	}
	return (a);
}
